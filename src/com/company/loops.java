package com.company;

public class loops {
    public static void main(String[] args) {
        //while loop
        int i = 1;
        while(i<=3){
            System.out.println(i);
            i++;
        }
        //do while loop
        int b = 0;
        do{
            System.out.println(b);
            b++;
        }while(b<5);
        // for loop
        for(int x = 1; x<=5; x++){
            System.out.println(x);
        }
    }
}
